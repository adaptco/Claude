# ── Outlook: Handoff Email ────────────────────────────────────────────────────
async def send_outlook_handoff(
    to_email: str,
    task_name: str,
    agent_id: str,
    stage: str,
    summary: str,
    word_doc_url: str = "",
    excel_url: str = "",
) -> dict[str, str]:
    """
    Sends a handoff email via Outlook/Exchange using Microsoft Graph.
    """
    token = await get_access_token()

    body = f"""
    <h2>🤖 A2A Digital Twin — Stage Complete: {stage}</h2>
    <p><strong>Task:</strong> {task_name}</p>
    <p><strong>Completed by:</strong> {agent_id}</p>
    <p><strong>Summary:</strong> {summary}</p>
    """
    if word_doc_url:
        body += f'<p>📄 <a href="{word_doc_url}">Handoff Document (Word)</a></p>'
    if excel_url:
        body += f'<p>📊 <a href="{excel_url}">Metrics Dashboard (Excel)</a></p>'
    body += "<hr/><p><em>Sent by A2A Digital Twin — adaptco-main/A2A_MCP</em></p>"

    email_payload = {
        "message": {
            "subject": f"[A2A Twin] Stage Complete: {stage} — {task_name}",
            "body": {"contentType": "HTML", "content": body},
            "toRecipients": [{"emailAddress": {"address": to_email}}],
        },
        "saveToSentItems": True,
    }

    async with httpx.AsyncClient() as client:
        resp = await client.post(
            f"{GRAPH_BASE}/users/{USER_EMAIL}/sendMail",
            headers=_auth_headers(token),
            json=email_payload,
        )
        resp.raise_for_status()

    return {"ok": True, "sent_to": to_email, "subject": email_payload["message"]["subject"]}


# ── Unified checkpoint writer ─────────────────────────────────────────────────
async def write_stage_checkpoint(
    task_name: str,
    agent_id: str,
    stage: str,
    summary: str,
    acceptance_criteria: list[str],
    fossil_hashes: list[str],
    metrics: dict[str, Any],
    checkpoint_type: str = "all",    # "word" | "excel" | "outlook" | "all"
    handoff_email: str = "",
    next_agent: str = "",
) -> dict[str, Any]:
    """
    Single call to write all Office checkpoints for a workflow stage completion.
    Called by IntentEngine after each stage finishes.
    """
    results: dict[str, Any] = {"stage": stage, "task": task_name}

    if checkpoint_type in ("word", "all"):
        results["word"] = await create_word_handoff(
            task_name, agent_id, stage, summary,
            acceptance_criteria, fossil_hashes, next_agent,
        )

    if checkpoint_type in ("excel", "all"):
        results["excel"] = await update_excel_metrics(metrics)

    if checkpoint_type in ("outlook", "all") and handoff_email:
        results["outlook"] = await send_outlook_handoff(
            to_email=handoff_email,
            task_name=task_name,
            agent_id=agent_id,
            stage=stage,
            summary=summary,
            word_doc_url=results.get("word", {}).get("url", ""),
            excel_url=results.get("excel", {}).get("workbook", ""),
        )

    return results
