"""
integrations/office/graph_checkpoint.py

Microsoft Office checkpoints via Microsoft Graph API.
No desktop required — pure REST.

Creates checkpoints at each Workflow Stage completion:
  - Word doc   → agent handoff report (mirrors HANDOFF.md pattern from A2A_MCP)
  - Excel      → metrics dashboard (test pass rate, task completion, coverage)
  - Outlook    → sends handoff email to next agent/human in workflow

Authentication: Azure AD App Registration with Graph API permissions:
  - Files.ReadWrite (for Word/Excel via OneDrive)
  - Mail.Send (for Outlook)
  - Sites.ReadWrite.All (for SharePoint if used)

Setup: set env vars OFFICE_TENANT_ID, OFFICE_CLIENT_ID, OFFICE_CLIENT_SECRET
"""

from __future__ import annotations

